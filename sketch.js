let selectedCharacter = '';
const pixelSize = 10;

function setup() {
  createCanvas(640, 480);
}

function draw() {
  background(200,0,0);

  //The outer lines with a big stroke weight to make the interface have a sort of retro look.
  stroke("red")
  strokeWeight(20)
  line(5,5,5,475)
  line(5,5,635,5)
  line(635,5,635,475)
  line(5,475,635,475)
  
  //The boxes for the (Mario, Luigi, Yoshi and Toad) buttons.
  stroke("red")
  strokeWeight(4)
  fill("red")
  rect(420, 120, 150, 50);
  rect(420, 180, 150, 50);
  rect(420, 240, 150, 50);
  rect(420, 300, 150, 50);

  //Text in button Mario
  strokeWeight(0)
  fill("white")
  textSize(32);
  text('Mario', 455, 155);

  //Text in button Luigi
  strokeWeight(0)
  fill("white")
  textSize(32);
  text('Luigi', 460, 215);

  //Text in button Yoshi
  strokeWeight(0)
  fill("white")
  textSize(32);
  text('Yoshi', 455, 275);
  
  //Text in button Toad
  strokeWeight(0)
  fill("white")
  textSize(32);
  text('Toad', 460, 335);

  //The if selected system.
  if (selectedCharacter == 'Mario') {
    drawArt(mario);
  } else if (selectedCharacter == 'Luigi') {
    drawArt(luigi);
  } else if (selectedCharacter == 'Yoshi') {
    drawArt(yoshi);
  } else if (selectedCharacter == 'Toad') {
    drawArt(toad);
  }
}

//The mouse pressed function.
function mousePressed() {
  if (mouseX > 420 && mouseX < 570) {
    if (mouseY > 120 && mouseY < 170) {
      selectedCharacter = 'Mario';
    } else if (mouseY > 180 && mouseY < 230) {
      selectedCharacter = 'Luigi';
    } else if (mouseY > 240 && mouseY < 290) {
      selectedCharacter = 'Yoshi';
    } else if (mouseY > 300 && mouseY < 350) {
      selectedCharacter = 'Toad';
    }
  }
}

//This draws the pixel art on the canvas.
async function drawArt(art) {
  if (selectedCharacter == 'Mario') {
    translate(90,80);
  }
  else if (selectedCharacter == 'Luigi') {
    translate(145,80);
  }
  else if (selectedCharacter == 'Yoshi') {
    translate(100,80);
  }
  else if (selectedCharacter == 'Toad') {
    translate(85,60);
  }

  const height = art.length
  const width = art[0].length

  for (let y = 0; y < height; y++) {
    for (let x = 0; x < width; x++) {
      const num = art[y][x]
      if (num > 0 && selectedCharacter == 'Mario') {
        const color = mariocolors[num]
      fill(color);
      rect(x * pixelSize, y * pixelSize, pixelSize, pixelSize);
      }
      else if (num > 0 && selectedCharacter == 'Luigi') {
        const color = luigicolors[num]
      fill(color);
      rect(x * pixelSize, y * pixelSize, pixelSize, pixelSize);
      }
      else if (num > 0 && selectedCharacter == 'Yoshi') {
        const color = yoshicolors[num]
      fill(color);
      rect(x * pixelSize, y * pixelSize, pixelSize, pixelSize);
      }
      else if (num > 0 && selectedCharacter == 'Toad') {
        const color = toadcolors[num]
      fill(color);
      rect(x * pixelSize, y * pixelSize, pixelSize, pixelSize);
      }
    }
  }
}